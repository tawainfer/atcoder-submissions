using namespace std;

int main() {
  cin.tie(nullptr);
  ios_base::sync_with_stdio(false);

  double a, b, x;
  cin >> a >> b >> x;

  double half = a * a * b / 2;
  double ans;

  if(x < half) ans = atan(a * b * b / (2.0 * x));
  else ans = atan((2.0 * b - 2.0 * x / (a * a)) / a);
  ans = ans / 3.1415926535 * 180;

  cout << fix(7) << ans;
  return 0;
}