using namespace std;

int main() {
  cin.tie(nullptr);
  ios_base::sync_with_stdio(false);

  ll n;
  cin >> n;

  if(n % 2 == 1) {
    cout << 0;
    return 0;
  }

  ll ans = n / 10;
  n /= 10;

  ll x = 5;
  while(x <= n) {
    ans += n / x;
    x *= 5;
  }

  cout << ans;
  return 0;
}